
package Datos;

public class Ingresar {
    private int intervalo;
    private int turno;
    private int vidaP;
    private int vidaR;
    private int ataqueP;
    private int ataqueR;
    
    public int getTurno() {
        return turno;
    }

    public void setTurno(int turno) {
        this.turno = turno;
    }

    public int getVidaP() {
        return vidaP;
    }

    public void setVidaP(int vidaP) {
        this.vidaP = vidaP;
    }

    public int getVidaR() {
        return vidaR;
    }

    public void setVidaR(int vidaR) {
        this.vidaR = vidaR;
    }

    public int getAtaqueP() {
        return ataqueP;
    }

    public void setAtaqueP(int ataqueP) {
        this.ataqueP = ataqueP;
    }

    public int getAtaqueR() {
        return ataqueR;
    }

    public void setAtaqueR(int ataqueR) {
        this.ataqueR = ataqueR;
    }

    public int getIntervalo() {
        return intervalo;
    }

    public void setIntervalo(int intervalo) {
        this.intervalo = intervalo;
    }
}
